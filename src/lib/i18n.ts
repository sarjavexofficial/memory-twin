// 各言語辞書はキー欠けを型エラーで検出できるよう Dict 型（=日本語辞書と同じ形）で書く
export type Dict = typeof ja;

export const strings = { ja, en, zh, ko, fr, pt };

export function useStrings() {
  const { settings } = useSettings();
  // 保存済み設定に未知の言語コードが入っていても（旧バージョンとの食い違い等）落ちないよう日本語に退避
  return strings[settings.language] ?? strings.ja;
}

// 記録に保存される内部タグ（AI検索・抽出・決定など）は言語に依存しない固定値のまま保存し、
// 表示するときだけ現在の言語のラベルへ変換する（集計フィルタを壊さないため）
export function displayTag(tag: string, L: Dict): string {
  return L.internalTags[tag] ?? tag;
}
